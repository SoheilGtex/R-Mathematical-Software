mle_coefs <- fit_mle$par
names(mle_coefs) <- c("Intercept(b0)", "C2(b1)", "C3(b2)", "C4(b3)",
                       "S2(b4)", "S3(b5)", "W(b6)", "Wt(b7)")
cat("\n--- ضرایب برآورد شده با روش MLE (nlminb) ---\n")
print(round(mle_coefs, 6))
 
## ------------------------------------------------------------------------
## گام ۴: مقایسه با تابع آماده glm
## ------------------------------------------------------------------------
 
# برازش مدل با استفاده از تابع glm با فرمول متغیرهای اصلی (نه دامی‌ها)
# چون glm به‌طور خودکار متغیرهای کیفی factor را دامی می‌کند
crabs$C <- as.factor(crabs$C)
crabs$S <- as.factor(crabs$S)
 
fit_glm <- glm(Sa ~ C + S + W + Wt, data = crabs, family = poisson(link = "log"))
 
cat("\n--- خلاصه مدل glm ---\n")
summary(fit_glm)
 
glm_coefs <- coef(fit_glm)
cat("\n--- ضرایب مدل glm ---\n")
print(round(glm_coefs, 6))
 
## ------------------------------------------------------------------------
## مقایسه نهایی ضرایب دو روش (nlminb دستی در برابر glm)
## ------------------------------------------------------------------------
 
comparison <- data.frame(
  Parameter   = c("Intercept", "C2", "C3", "C4", "S2", "S3", "W", "Wt"),
  MLE_nlminb  = round(as.numeric(mle_coefs), 6),
