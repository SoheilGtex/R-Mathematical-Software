# تمرین تحویلی - روش دوم برآورد پارامترهای مدل رگرسیون پواسون
# بیشینه‌سازی تابع درست‌نمایی (Maximum Likelihood Estimation - MLE)
#
# داده: Crabs.csv  (ستون‌ها: C, S, W, Wt, Sa)
#   C  : متغیر رنگ (کیفی، ۴ سطح: 1,2,3,4)
#   S  : وضعیت ستون فقرات/خار پشتی (کیفی، ۳ سطح: 1,2,3)
#   W  : عرض پوسته (کمی - عددی)
#   Wt : وزن خرچنگ (کمی - عددی)
#   Sa : تعداد ماهواره‌ها (satellites) - متغیر پاسخ (شمارشی، پواسون)
#
# مدل:
#   log(lambda_i) = b0 + b1*C2_i + b2*C3_i + b3*C4_i + b4*S2_i + b5*S3_i
#                     + b6*W_i + b7*Wt_i
##############################################################################
 
## ------------------------------------------------------------------------
## گام ۰: خواندن داده و آماده‌سازی
## ------------------------------------------------------------------------
 
crabs <- read.csv("Crabs.csv", header = TRUE, fileEncoding = "UTF-8-BOM")
 
# مشاهده ساختار داده
str(crabs)
head(crabs)
 
## ------------------------------------------------------------------------
## گام ۱: آماده‌سازی داده‌ها و کدگذاری متغیرهای کیفی (Dummy Coding)
## ------------------------------------------------------------------------
 
# متغیر رنگ C دارای ۴ سطح است => k-1 = 3 متغیر مجازی
# سطح ۱ به عنوان سطح مرجع (Reference Level) در نظر گرفته می‌شود
crabs$C2 <- ifelse(crabs$C == 2, 1, 0)
crabs$C3 <- ifelse(crabs$C == 3, 1, 0)
crabs$C4 <- ifelse(crabs$C == 4, 1, 0)
 
# متغیر وضعیت ستون فقرات S دارای ۳ سطح است => k-1 = 2 متغیر مجازی
# سطح ۱ به عنوان سطح مرجع در نظر گرفته می‌شود
crabs$S2 <- ifelse(crabs$S == 2, 1, 0)
crabs$S3 <- ifelse(crabs$S == 3, 1, 0)
 
# بررسی نتیجه کدگذاری
head(crabs[, c("C", "C2", "C3", "C4", "S", "S2", "S3")])
 
## ------------------------------------------------------------------------
## گام ۲: نوشتن تابع درست‌نمایی منفی (Negative Log-Likelihood)
## ------------------------------------------------------------------------
#
# ورودی:
#   theta : بردار ۸ پارامتری شامل (b0, b1, b2, b3, b4, b5, b6, b7)
#   data  : دیتافریمی که شامل ستون‌های C2, C3, C4, S2, S3, W, Wt, Sa است
#
# خروجی:
#   منفیِ مجموع لگاریتم درست‌نمایی (که باید کمینه شود)
 
nloglik_loglinear <- function(theta, data) {
 
  # ۱. تفکیک بردار theta به ۸ پارامتر مدل
  b0 <- theta[1]
  b1 <- theta[2]
  b2 <- theta[3]
  b3 <- theta[4]
  b4 <- theta[5]
  b5 <- theta[6]
  b6 <- theta[7]
  b7 <- theta[8]
 
  # استخراج متغیرها از دیتافریم برای خوانایی بیشتر
  C2 <- data$C2
  C3 <- data$C3
  C4 <- data$C4
  S2 <- data$S2
  S3 <- data$S3
  W  <- data$W
  Wt <- data$Wt
  Sa <- data$Sa
 
  n <- length(Sa)                 # تعداد مشاهدات
  loglik_vec <- numeric(n)        # بردار ذخیره لگاریتم درست‌نمایی هر مشاهده
 
  # ۲. حلقه for برای محاسبه درست‌نمایی هر مشاهده
  for (i in 1:n) {
 
    # ۳. محاسبه پیش‌بین خطی (linear predictor) برای مشاهده i-ام
    linear_predictor <- b0 + b1 * C2[i] + b2 * C3[i] + b3 * C4[i] +
                         b4 * S2[i] + b5 * S3[i] +
                         b6 * W[i]  + b7 * Wt[i]
 
    # ۴. محاسبه lambda_i با استفاده از تابع نمایی
    lambda_i <- exp(linear_predictor)
 
    # ۵. محاسبه احتمال مشاهده‌شدن مقدار واقعی Sa[i] با فرض توزیع پواسون
    #    از تابع آماده dpois استفاده می‌کنیم (log = TRUE برای پایداری عددی)
    loglik_vec[i] <- dpois(Sa[i], lambda = lambda_i, log = TRUE)
  }
 
  # ۶. مجموع لگاریتم درست‌نمایی تمام مشاهدات و منفی کردن آن
  #    (چون بهینه‌سازها معمولاً "کمینه‌ساز" هستند، منفیِ log-likelihood را
  #     برمی‌گردانیم تا کمینه کردن آن معادل بیشینه کردن درست‌نمایی باشد)
  neg_loglik <- -sum(loglik_vec)
 
  return(neg_loglik)
}
##############################################################################
# Step 2 : Negative Log-Likelihood Function
##############################################################################

nloglik_loglinear <- function(theta, data){

  ## Model Parameters
  b0 <- theta[1]
  b1 <- theta[2]
  b2 <- theta[3]
  b3 <- theta[4]
  b4 <- theta[5]
  b5 <- theta[6]
  b6 <- theta[7]
  b7 <- theta[8]

  ## Extract Variables
  C2 <- data$C2
  C3 <- data$C3
  C4 <- data$C4
  S2 <- data$S2
  S3 <- data$S3
  W  <- data$W
  Wt <- data$Wt
  Sa <- data$Sa

  ## Number of observations
  n <- length(Sa)

  ## Store log-likelihood values
  loglik_vec <- numeric(n)

  ## Loop over observations
  for(i in seq_len(n)){

    eta <- b0 +
           b1*C2[i] +
           b2*C3[i] +
           b3*C4[i] +
           b4*S2[i] +
           b5*S3[i] +
           b6*W[i] +
           b7*Wt[i]

    lambda_i <- exp(eta)

    ## Manual Poisson Log-Likelihood
    loglik_vec[i] <-
      Sa[i]*log(lambda_i) -
      lambda_i -
      lgamma(Sa[i] + 1)

  }

  return(-sum(loglik_vec))

}
 
## ------------------------------------------------------------------------
## گام ۳: بهینه‌سازی عددی با استفاده از nlminb
## ------------------------------------------------------------------------
 
# نقطه شروع (Initial Values):
#   b0 = log(میانگین کل داده‌ها)  چون در مدل پواسون وقتی تمام متغیرها صفرند
#        lambda = exp(b0) است، پس بهترین حدس اولیه برای b0 برابر است با:
#        log(mean(Sa))
#   سایر ضرایب (b1 تا b7) را صفر در نظر می‌گیریم.
 
theta0 <- c(
  b0 = log(mean(crabs$Sa)),
  b1 = 0,
  b2 = 0,
  b3 = 0,
  b4 = 0,
  b5 = 0,
  b6 = 0,
  b7 = 0
)
 
 ##############################################################################
# Step 3 : Maximum Likelihood Estimation using nlminb
##############################################################################

## Initial parameter values

theta0 <- c(
  b0 = log(mean(crabs$Sa)),
  b1 = 0,
  b2 = 0,
  b3 = 0,
  b4 = 0,
  b5 = 0,
  b6 = 0,
  b7 = 0
)

cat("\nInitial Values\n")
print(theta0)

##############################################################################
# Numerical Optimization
##############################################################################

fit_mle <- nlminb(

  start = theta0,

  objective = nloglik_loglinear,

  data = crabs,

  lower = rep(-Inf,8),

  upper = rep( Inf,8)

)

##############################################################################
# Convergence Check
##############################################################################

if(fit_mle$convergence==0){

  cat("\nOptimization converged successfully.\n")

}else{

  warning("Optimization did not converge.")

}

##############################################################################
# Estimated Parameters
##############################################################################

