# پاسخ به پرسش نهایی تمرین:
#
# «آیا ضرایب دقیقاً یکی هستند؟ اگر تفاوت ناچیزی در حد اعشار دارند،
#  به نظر شما علت آن چیست؟»
#
# پاسخ (به عنوان راهنما برای درج در گزارش پروژه):
#
# ۱. هر دو روش (بهینه‌سازی دستی درست‌نمایی با nlminb، و تابع آماده glm که
#    داخلش از روش Iteratively Reweighted Least Squares/Fisher Scoring
#    استفاده می‌کند) در حال حل یک مسئله بهینه‌سازی ریاضی یکسان هستند: هر دو
#    به‌دنبال بیشینه کردن (یا کمینه کردن منفیِ) تابع درست‌نمایی پواسون
#    هستند. بنابراین از نظر تئوری، نقطه بهینه (جواب) باید یکسان باشد.
#
# ۲. تفاوت بسیار جزئی که معمولاً در حد ۴ تا ۶ رقم اعشار مشاهده می‌شود،
#    ناشی از تفاوت در الگوریتم‌های عددی بهینه‌سازی است:
#      - glm از الگوریتم IRLS/Fisher Scoring استفاده می‌کند که مشتقات
#        تحلیلی (Gradient و Hessian دقیق تابع درست‌نمایی نمایی خانواده)
#        را به صورت بسته (closed-form) محاسبه می‌کند.
#      - nlminb یک الگوریتم بهینه‌سازی عمومی (Quasi-Newton) است که از
#        مشتقات عددی (Numerical Derivatives) به جای مشتقات تحلیلی
#        استفاده می‌کند (مگر آنکه گرادیان تحلیلی به آن داده شود).
#
# ۳. معیارهای همگرایی (Convergence Tolerance) در دو الگوریتم متفاوت است؛
#    یعنی هر الگوریتم زمانی متوقف می‌شود که تغییرات تابع هدف یا پارامترها
#    از یک آستانه بسیار کوچک (مثلاً 1e-8 یا 1e-10) کمتر شود، و این آستانه‌ها
#    در glm و nlminb یکسان تنظیم نشده‌اند.
#
# ۴. خطاهای رُند کردن اعداد اعشاری (Floating-point Rounding Error) در
#    محاسبات کامپیوتری نیز به طور طبیعی باعث تفاوت‌های بسیار کوچک
#    (در حد e-6 تا e-10) می‌شود.
#
# جمع‌بندی: تفاوت مشاهده‌شده صرفاً یک خطای عددی ناشی از تفاوت الگوریتم‌های
# بهینه‌سازی و معیار توقف آن‌هاست، نه یک اختلاف مفهومی در مدل؛ از نظر
# آماری و عملی، دو مجموعه ضرایب معادل (Equivalent) در نظر گرفته می‌شوند.
